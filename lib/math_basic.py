def add(_a: float, _b: float) -> float:
    return _a + _b


def subtract(_a: float, _b: float) -> float:
    return _a - _b


def multiply(_a: float, _b: float) -> float:
    return _a * _b


def divide(_a: float, _b: float) -> float:
    return _a / _b


def power(_a: int, _b: int) -> int:
    return _a ** _b